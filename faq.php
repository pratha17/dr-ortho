                                <h1>Post Operative Info:</h1>
                                <h3>Will every patient after joint replacement surgery go home the next day?</h3>
                                <p>The main aim of our pain management protocol is to enable patients to return to their home environment doing activities of daily living as soon as possible.</p>
                                <p>The majority of patients will be able to go home the next day, some have actually gone home the same day, but they are younger, fit patients who are very motivated.</p>
                                <p>The factors that determine early discharge are patient physiology after surgery (status of blood pressure, blood transfusion requirements, fluid balance, etc) and also the presence of a carer at home. We have avoided using narcotics in the early phase of our program to avoid sedation that slows down early independent mobilisation.</p>
                                <p>Another reason for discharge after an extra day in hospital is because some Private Health Funds will not pay the Hospital for “Joint Replacement” as they (Health Funds) classify joint replacements as major surgery requiring at least 3-7 days hospital stay. Patients can check with their health funds, and hospitals usually work together with the patients and surgeons to provide the best outcome. If this is the case, patients can certainly be “street ready” the day after surgery and go home or outside on “gate-leave”.This program is most importantly Good for the Patient, Good for the Surgeon, Good for the Hospital (frees up beds for other patients who may need emergency surgical care etc) and Good For the Fund.</p>
                            
                                <h3>Do I need Physiotherapy?</h3>
                                <p>Generally patients having Total Hip Replacements and Hip Resurfacing will be given instructions and need NOT have inpatient rehabilitation as they will most likely be discharged home the following day. Patients after Knee Replacements/Knee Reconstructions and Revision Surgery may need to organise out-patient physiotherapy after hospital discharge. Our team will discuss this before your surgery. No inpatient rehab will be required.</p>
                                <h3>How long do I need crutches for?</h3>
                                <p>This would depend on the type of operation and patient confidence. Dr Morse and the team will guide you through this. In general most patients go home with 2 crutches and gradually move on to 1 crutch before discarding it. This may take anywhere from 1-3 weeks.</p>
                                                            
                                <h3>When can I go to Work?</h3>
                                <p>This will depend on the nature, type and complexity of surgery, patient motivation and the type of work involved. Some patients have gone to work almost 3-4 days after joint replacements (office type duties) whilst others prefer to convalesce for a short period (1-3 weeks) before commencing work activities as this may involve self-driving. Dr Morse will discuss this individually with patients.</p>
                                <h3>When Can I resume sports like Golf?</h3>
                                <p>This will depend on the nature of the surgical procedure, complexity and the type of joint and patient physiology and motivation. Some patients with this LIA pain management program have gone to the golf course as early as 3 days after joint replacement (putting only). Dr Morse will discuss this individually with patients.</p>


